package center.kit.app.homework.lesson4.util.convertors;

public class PrimitiveConvertor {

    public char floatToChar(float float1)
    {
        return (char)float1 ;
    }

    public char intToChar(int int1)
    {
        return (char)int1;
    }

    public int charToInt(char char1)
    {
        return (int)char1;
    }

}
